import java.util.*;

class Ex11_7 {
	public static void main(String[] args) {
		String[] strArr = {"cat","Dog","lion","tiger"};
		
		Arrays.sort(strArr); //String의 Comparable구현에 의한 정렬(기본정렬기준은 사전순서(대문자->소문자))
		System.out.println("strArr="+Arrays.toString(strArr));
		
		Arrays.sort(strArr, String.CASE_INSENSITIVE_ORDER); //대소문자 구분않는 정렬 기준(comparator)
		System.out.println("strArr="+Arrays.toString(strArr));
		
		Arrays.sort(strArr, new Descending()); //역순 정렬(내림차순)
		System.out.println("strArr="+Arrays.toString(strArr));
		
	}
}

class Descending implements Comparator {
	public int compare(Object o1, Object o2) {
		if(o1 instanceof Comparable && o2 instanceof Comparable) {
			Comparable c1 = (Comparable)o1;
			Comparable c2 = (Comparable)o2;
			return c1.compareTo(c2)*(-1); //기본정렬기준에 -1을 곱해서 기본 정렬방식의 역으로 변경
											// 또는 c2.compareTo(c1)으로 해도 됨
		}
		return -1;
	}
}

	